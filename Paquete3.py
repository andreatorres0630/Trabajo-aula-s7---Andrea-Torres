# PAQUETE STATISTICS
# El paquete statistics proporciona funciones integradas para cálculos estadísticos básicos, 
# como media, mediana y desviación estándar, aplicables a diversos tipos numéricos.

# EJEMPLO
import statistics

ventas = [120, 150, 100, 180, 200]
promedio = statistics.mean(ventas)

print(f"El promedio de ventas es de: {promedio}")

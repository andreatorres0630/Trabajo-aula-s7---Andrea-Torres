# PAQUETE RANDOM
# El paquete random genera números pseudoaleatorios y proporciona funciones para realizar operaciones de aleatoriedad, 
# como seleccionar elementos al azar de una lista o generar números enteros o flotantes dentro de un rango específico.

# EJEMPLO
import random

dado = random.randint(1, 6)
print(f"El dado cayó en: {dado}")